    private void jButton2_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_searchActionPerformed

        try {
            if (!txt_search.getText().equals("")) {

                byte d = 0;

                String Name[] = txt_search.getText().split(" ");
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                Vector v = new Vector();
                ResultSet rs = Class_Files.DB.search("SELECT * FROM employee_reg WHERE First_Name = '" + Name[0] + "' && Last_Name='" + Name[1] + "' ");
                while (rs.next()) {
                    d = 1;
                    v.add(rs.getString("First_Name"));
                    v.add(rs.getString("Last_Name"));
                    v.add(rs.getString("Nic"));
                    v.add(rs.getString("Gender"));
                    v.add(rs.getString("Address"));
                    v.add(rs.getString("Contact"));
                    v.add(rs.getString("Email"));
                    v.add(rs.getString("Emp_Type"));

                    dtm.addRow(v);
                }

                if (d == 0) {
                    JOptionPane.showMessageDialog(this, "Sorry No result found..!");
                }

                txt_search.setText("");
                jButton2_search.setEnabled(false);
                EmtyTF();
                txtFname.grabFocus();

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton2_searchActionPerformed

