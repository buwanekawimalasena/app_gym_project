    private void txtFnameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFnameKeyReleased

        try {

            String EMAIL_REGEX = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";

            Boolean b = txtEmail.getText().matches(EMAIL_REGEX);

            if (txtFname.getText().length() == 0) {
                jButton5_Save.setEnabled(false);
                jButton4_Update.setEnabled(false);
            } else {

                if (txtFname.getText().length() != 0
                        && txtLname.getText().length() != 0
                        && txtNic.getText().length() != 0
                        && jComboBox1.getSelectedIndex() != 0
                        && txtAddress.getText().length() != 0
                        && txtContact.getText().length() != 0
                        && txtEmail.getText().length() != 0
                        && jComboBox2.getSelectedIndex() != 0
                        && !path.equals("") && b) {

                    jButton5_Save.setEnabled(true);
                    jButton4_Update.setEnabled(true);

                } else {
                    jButton5_Save.setEnabled(false);
                    jButton4_Update.setEnabled(false);
                }

            }

            /////////////////////////////////////////
            if (i == 10) {
                jButton5_Save.setEnabled(false);
            } else {
                jButton4_Update.setEnabled(false);
            }

            ///Grab Focus
            if (evt.getKeyCode() == 10) {

                txtLname.grabFocus();
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_txtFnameKeyReleased

